import matplotlib.pyplot as plt

# Дані для графіка (x та y координати точок)
x = [2024, 2025, 2026, 2027, 2028, 2029, 2030, 2031, 2032, 2033, 2034]
y = [619, 593, 568, 524, 491, 453, 425, 409, 386, 360, 351]

# Створення лінійного графіка
plt.plot(x, y)

# Додавання заголовка та підписів осей
plt.title('Зменшення кількості навчальних закладів 2024-2034')
plt.xlabel('роки')
plt.ylabel('кількість навчальних закладів')

plt.grid()

# Відображення графіка
plt.show()
