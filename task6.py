import matplotlib.pyplot as plt

# Дані для графіка (x та y координати стовпців)
x = ['3 роки', '5 років', '10 років', '15 років', '20 років', '25+ років']
y = [40, 10, 20, 5, 30, 15]

# Створення стовпчикового графіка
plt.bar(x, y)

# Додавання заголовка та підписів осей
plt.title('Залежність розміру надбавки від стажу')
plt.xlabel('Розмір надбавки (%)')
plt.ylabel('Стаж (роки)')

plt.grid()

# Відображення графіка
plt.show()
