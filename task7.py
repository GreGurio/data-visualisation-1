import matplotlib.pyplot as plt

# Дані для графіка (x та y координати стовпців)
x = ['18-25', '25-30', '30-35', '35-40', '40-45', '45+']
y = [13, 10, 11, 15, 12, 10]

# Створення стовпчикового графіка
plt.bar(x, y)

# Додавання заголовка та підписів осей
plt.title('Плинність кадрів по віковим групам')
plt.xlabel('Вікові групи')
plt.ylabel('Плинність (%)')

plt.grid()

# Відображення графіка
plt.show()
