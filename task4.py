import matplotlib.pyplot as plt

# Дані для графіка (x та y координати стовпців)
x = ['Підрозділ 1', 'Підрозділ 2', 'Підрозділ 3', 'Підрозділ 4', 'Підрозділ 5', 'Підрозділ 6']
y = [13 ,15, 13, 13, 14, 15]

# Створення стовпчикового графіка
plt.bar(x, y)

# Додавання заголовка та підписів осей
plt.title('Плинність кадрів по підрозділах')
plt.xlabel('Підрозділи')
plt.ylabel('Плинність кадрів (%)')

plt.grid()

# Відображення графіка
plt.show()
