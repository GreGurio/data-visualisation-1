import matplotlib.pyplot as plt

# Дані для графіка (x та y координати точок)
x = ['Січ.', 'Лют.', 'Бер.', 'Кв.', 'Тр.',  'Чер.', 'Лип.', 'Сер.', 'Вер.', 'Жов.', 'Лис.', 'Гр.']
y = [7234,7124,6931,6812,6789,6514,6315,6162,5801,5736,5517,5326]

# Створення лінійного графіка
plt.plot(x, y)

# Додавання заголовка та підписів осей
plt.title('Прибутновість акцій за 2023 рік')
plt.xlabel('місяці')
plt.ylabel('тис. грн.')

plt.grid()

# Відображення графіка
plt.show()
