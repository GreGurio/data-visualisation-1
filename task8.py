import matplotlib.pyplot as plt

# Дані для графіка (x та y координати стовпців)
x = ['Центральний', 'Південний', 'Східний', 'Західний']
y = [42000, 83000, 64000, 73000]

# Створення стовпчикового графіка
plt.bar(x, y)

# Додавання заголовка та підписів осей
plt.title('Народжуваність по регіонам')
plt.xlabel('Регіони')
plt.ylabel('Кількість дітей')

plt.grid()

# Відображення графіка
plt.show()
