import matplotlib.pyplot as plt

# Дані для графіка (x та y координати стовпців)
x = ['Андрій', 'Олексій', 'Олександр', 'Богдан', 'Володимир', 'Ростислав', 'Владислав', 'Дмитро']
y = [98, 76, 85, 100, 87, 84, 83, 82]

# Створення стовпчикового графіка
plt.bar(x, y)

# Додавання заголовка та підписів осей
plt.title('Рейтинги студентів')
plt.xlabel('Студенти')
plt.ylabel('Кількість дітей')

plt.grid()

# Відображення графіка
plt.show()
